#!/bin/sh
cd ${CONFIGURATION_BUILD_DIR}
tar -cvzf ${EXECUTABLE_FOLDER_PATH}.tar.gz ${EXECUTABLE_FOLDER_PATH}

mv ${EXECUTABLE_FOLDER_PATH}.tar.gz ${PROJECT_DIR}/${EXECUTABLE_FOLDER_PATH}.tar.gz

cd ${PROJECT_DIR}

scp ${EXECUTABLE_FOLDER_PATH}.tar.gz "${ATV_SSH_USER}@${ATV_ADDRESS}:${ATV_APPLIANCE_INSTALL_PATH}/${EXECUTABLE_FOLDER_PATH}.tar.gz"

ssh ${ATV_SSH_USER}@${ATV_ADDRESS} "cd ${ATV_APPLIANCE_INSTALL_PATH}; tar -xvzf ${ATV_APPLIANCE_INSTALL_PATH}/${EXECUTABLE_FOLDER_PATH}.tar.gz"

#ssh ${ATV_SSH_USER}@${ATV_ADDRESS} "rm -f  ${ATV_APPLIANCE_INSTALL_PATH}/${EXECUTABLE_FOLDER_PATH}.tar.gz"

# Reboot to complete the installation
ssh ${ATV_SSH_USER}@${ATV_ADDRESS} killall -9 AppleTV 
